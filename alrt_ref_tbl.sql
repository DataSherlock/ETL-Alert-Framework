CREATE TABLE ALRT_REF_TBL
(
  ALRT_KEY     NUMBER                           NOT NULL,
  ALRT_CD      VARCHAR2(5 BYTE)                 NOT NULL,
  ALRT_DESC    VARCHAR2(500 BYTE),
  ALRT_SUBJ    VARCHAR2(500 BYTE),
